package com.alpha.tpcsfashion.beans;

public class DynamicFieldEvents {

	private int dynamicFieldId;
	private String pagefieldname;
	private String functionName;
	private String attachedJS;
	private String eventName;
	private String pickListOption;
	private int PickListSortOrder;
	public int getDynamicFieldId() {
		return dynamicFieldId;
	}
	public void setDynamicFieldId(int dynamicFieldId) {
		this.dynamicFieldId = dynamicFieldId;
	}
	public String getPagefieldname() {
		return pagefieldname;
	}
	public void setPagefieldname(String pagefieldname) {
		this.pagefieldname = pagefieldname;
	}
	public String getFunctionName() {
		return functionName;
	}
	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}
	public String getAttachedJS() {
		return attachedJS;
	}
	public void setAttachedJS(String attachedJS) {
		this.attachedJS = attachedJS;
	}
	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public void setPickListOption(String pickListOption) {
		this.pickListOption = pickListOption;
	}
	public String getPickListOption() {
		return pickListOption;
	}
	public void setPickListSortOrder(int pickListSortOrder) {
		PickListSortOrder = pickListSortOrder;
	}
	public int getPickListSortOrder() {
		return PickListSortOrder;
	}
	
	

}
